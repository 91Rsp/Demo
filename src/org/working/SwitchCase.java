package org.working;

public class SwitchCase {
	
	public static void main(String[] args) {
		 String day="saturday";
		  switch (day) {
		  
		  case "Monday":System.out.println("working day");
		  break;
		  case "tuesday":System.out.println( "second day of the week");
			  
			
			break;

		default:System.out.println("weekend");
			break;
		}
	}

}
